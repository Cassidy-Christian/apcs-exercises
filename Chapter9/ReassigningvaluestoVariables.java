public class ReassigningvaluestoVariables
{  
    public static void main ( String[] args )  
  {
      int x = 0; 
      
      x = (3 * x * x) - (8 * x) + 4;

      int z = 2; 
      z = (3 * z * z) - (8 * z) + 4;

      double H = 4.0;
      H = (3 * H * H) - (8 * H) + 4;


      System.out.println(x);

      System.out.println(z);

      System.out.println(H);
  }
}


        